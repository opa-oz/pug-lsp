func GetAttributes(tagName string) *[]string {
	arr, ok := tagToAttributes[HtmlTag(tagName)]

	if !ok {
		return nil
	}

	return arr
}

func GetTags() *[]string {
	keys := make([]string, 0, len(tagToAttributes))
	for k := range tagToAttributes {
		htmlK := string(k)
		keys = append(keys, htmlK)
	}

	return &keys
}
